import java.util.Scanner;

class Operators
{
    public static void main(String args[]){
    
        int girls, boys, people;
        girls = 6;
        boys = 3;
        people = girls + boys; /*-, /, *, % are possible */
        System.out.println(people);
        
        int a=10,b=10;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
        
        
    }
}