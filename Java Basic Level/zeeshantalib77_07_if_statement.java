class if_statement
{
    public static void main(String args[])
    {
        int number = 6;
        if (number == 9)  // executed when condition is true other wise in case of false else will be executed
        {    
            System.out.println("Yes this is right number");
        }
        else
        {
            System.out.println("No this is wrong number");    
        }
    }
}