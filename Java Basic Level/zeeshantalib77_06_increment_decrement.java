class Increment_Decrement
{
    public static void main(String args[])
    {   
        int a=10,b=10;   
        System.out.println(a++);
        System.out.println(++a);
        System.out.println(b--);
        System.out.println(--b);
        
        a+=5; // you can also use * and / etc
        b-=5;
        
        System.out.println(a);
        System.out.println(b);

    }
}